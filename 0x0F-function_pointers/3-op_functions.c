#include "3-calc.h"
/**
 * op_add - sum of two num
 * @a: Variable from main
 * @b: Variable from main
 * Return: Return sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub of two num
 * @a: Variable from main
 * @b: Variable from main
 * Return: Return sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multi of two num
 * @a: Variable from main
 * @b: Variable from main
 * Return: Return multi
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div of two num
 * @a: Variable from main
 * @b: Variable from main
 * Return: Return div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of two num
 * @a: Variable from main
 * @b: Variable from main
 * Return: Return mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
